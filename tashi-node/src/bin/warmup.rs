/// src/bin/warmup.rs
///
/// P2P warm-up demo: discovery · heartbeats · replicated state · failure recovery
///
/// IMPORTANT – quorum requirement
/// ───────────────────────────────
/// Tashi Vertex uses BFT consensus.  ALL nodes listed in the config must be
/// running before any transaction can commit (with a 3-node config you need
/// all 3 online).  Use `gen_warmup_config` to create a dedicated 3-node
/// warmup_config.json, then run:
///
///   cargo run --bin warmup -- --id 1 --config warmup   # Agent A
///   cargo run --bin warmup -- --id 2 --config warmup   # Agent B
///   cargo run --bin warmup -- --id 3 --config warmup   # silent quorum

use clap::Parser;
use serde::{Deserialize, Serialize};
use std::collections::HashMap;
use std::env;
use std::fs;
use std::path::PathBuf;
use std::time::{SystemTime, UNIX_EPOCH};
use tokio::time::{interval, sleep_until, Duration, Instant};

use tashi_vertex::{Context, Engine, KeyPublic, KeySecret, Message, Options, Peers, Socket, Transaction};

// ── Config path resolver ───────────────────────────────────────────────────────
fn find_config(stem: &str) -> PathBuf {
    let filename = format!("{}.json", stem);
    let mut dir = env::current_dir().expect("cwd");
    loop {
        if dir.join("Cargo.lock").exists() {
            return dir.join(&filename);
        }
        if !dir.pop() {
            return env::current_dir().unwrap().join(&filename);
        }
    }
}

// ── CLI ────────────────────────────────────────────────────────────────────────
#[derive(Parser)]
#[clap(about = "P2P warm-up: discovery · heartbeats · shared state · failure recovery")]
struct Args {
    /// Node ID matching an entry in the config file.
    #[clap(long)]
    id: u16,

    /// Config file stem (without .json).  Use "warmup" for the dedicated
    /// warmup_config.json generated by gen_warmup_config.
    #[clap(long, default_value = "warmup_config")]
    config: String,

    /// Only Agent A (id=1) auto-toggles its role after this many seconds.
    /// Set to 0 to disable. (default: 20)
    #[clap(long, default_value = "20")]
    toggle_after: u64,

    /// Mark peers stale if silent for this many seconds. (default: 8)
    #[clap(long, default_value = "8")]
    stale_secs: u64,

    /// Silent quorum mode: participate in consensus without demo output.
    /// Use for node 3+ that are only needed to satisfy the quorum.
    #[clap(long)]
    silent: bool,
}

// ── Config schema ──────────────────────────────────────────────────────────────
#[derive(Deserialize)]
struct NodeConfig {
    id: u16,
    port: u16,
    secret_key: String,
    public_key: String,
}

// ── Shared state ───────────────────────────────────────────────────────────────
#[derive(Serialize, Deserialize, Debug, Clone, PartialEq)]
#[serde(rename_all = "snake_case")]
enum Role { Scout, Carrier }

#[derive(Serialize, Deserialize, Debug, Clone, PartialEq)]
#[serde(rename_all = "snake_case")]
enum Status { Ready, Busy }

/// The tiny replicated JSON blob required by the acceptance criteria.
#[derive(Serialize, Deserialize, Debug, Clone)]
struct AgentState {
    peer_id:      String,
    last_seen_ms: u64,
    role:         Role,
    status:       Status,
}

// ── Wire protocol ──────────────────────────────────────────────────────────────
#[derive(Serialize, Deserialize, Debug)]
enum SwarmMessage {
    /// Broadcast on startup (and after consensus forms).
    Hello(AgentState),
    /// Sent in direct response to receiving a Hello from a new peer.
    /// Completes the two-way handshake required by the spec.
    HelloAck(AgentState),
    /// Sent every HEARTBEAT_SECS to prove liveness.
    Heartbeat(AgentState),
    /// Sent whenever a node's state changes (e.g. role toggle).
    StateUpdate(AgentState),
}

// ── Helpers ────────────────────────────────────────────────────────────────────
fn now_ms() -> u64 {
    SystemTime::now().duration_since(UNIX_EPOCH).unwrap().as_millis() as u64
}

fn short(key: &str) -> &str {
    &key[key.len().saturating_sub(8)..]
}

fn send_swarm_msg(engine: &Engine, msg: &SwarmMessage) {
    if let Ok(bytes) = bincode::serialize(msg) {
        let mut tx = Transaction::allocate(bytes.len());
        tx.copy_from_slice(&bytes);
        let _ = engine.send_transaction(tx);
    }
}

fn sep() { println!("{}", "─".repeat(72)); }

// ── Entry point ────────────────────────────────────────────────────────────────
#[tokio::main(flavor = "current_thread")]
async fn main() -> anyhow::Result<()> {
    let args = Args::parse();
    let stale_ms = args.stale_secs * 1_000;

    // 1. Load config ─────────────────────────────────────────────────────────
    let config_path = find_config(&args.config);
    if !config_path.exists() {
        eprintln!("❌  Config {:?} not found.", config_path);
        eprintln!("    Run:  cargo run --bin gen_warmup_config");
        std::process::exit(1);
    }
    let configs: Vec<NodeConfig> = serde_json::from_str(&fs::read_to_string(&config_path)?)?;
    let my_config = configs.iter().find(|c| c.id == args.id)
        .ok_or_else(|| anyhow::anyhow!("❌ Node ID {} not in config", args.id))?;

    // 2. Bootstrap Tashi Vertex ───────────────────────────────────────────────
    let secret_key = my_config.secret_key.parse::<KeySecret>()?;
    let bind_addr = format!("127.0.0.1:{}", my_config.port);

    let context = Box::leak(Box::new(Context::new()?));
    let secret  = Box::leak(Box::new(secret_key));
    let socket  = Socket::bind(context, &bind_addr).await?;

    let mut options = Options::new();
    options.set_report_gossip_events(true);

    let mut peers = Peers::with_capacity(configs.len())?;
    for c in &configs {
        if c.id != my_config.id {
            let pub_key = c.public_key.parse::<KeyPublic>()?;
            peers.insert(&format!("127.0.0.1:{}", c.port), &pub_key, Default::default())?;
        }
    }
    peers.insert(&bind_addr, &secret.public(), Default::default())?;

    sep();
    if args.silent {
        println!(
            "🔇  Node {} (quorum) online │ port {} │ key …{}",
            my_config.id, my_config.port, short(&my_config.public_key)
        );
        println!("    Participating in consensus silently.");
    } else {
        println!(
            "🚀  Node {} online │ port {} │ key …{}",
            my_config.id, my_config.port, short(&my_config.public_key)
        );
        println!(
            "    Config: {:?} │ {} peers configured",
            config_path.file_name().unwrap(),
            configs.len() - 1
        );
    }
    sep();

    let engine = Engine::start(context, socket, options, secret, peers, false)?;

    // ── Silent quorum path (node 3+): just drain the engine, nothing else ────
    if args.silent {
        while let Ok(Some(_)) = engine.recv_message().await {}
        return Ok(());
    }

    // 3. Initial state ────────────────────────────────────────────────────────
    let mut my_state = AgentState {
        peer_id:      my_config.public_key.clone(),
        last_seen_ms: now_ms(),
        role:         if args.id % 2 == 1 { Role::Carrier } else { Role::Scout },
        status:       Status::Ready,
    };
    let mut peer_map: HashMap<String, AgentState> = HashMap::new();

    // 4. Broadcast signed Hello ───────────────────────────────────────────────
    send_swarm_msg(&engine, &SwarmMessage::Hello(my_state.clone()));
    println!(
        "🤝  [HELLO]  Broadcast signed hello │ role: {:?} │ status: {:?}",
        my_state.role, my_state.status
    );
    println!("    Waiting for consensus to form (all {} nodes must be online)…", configs.len());

    // 5. Timers ───────────────────────────────────────────────────────────────
    let mut hb_tick    = interval(Duration::from_secs(3));
    let mut stale_tick = interval(Duration::from_secs(5));

    let toggle_at: Option<Instant> = if args.id == 1 && args.toggle_after > 0 {
        Some(Instant::now() + Duration::from_secs(args.toggle_after))
    } else {
        None
    };
    let mut role_toggled = false;

    // ── Main event loop ───────────────────────────────────────────────────────
    loop {
        tokio::select! {

            // ── Heartbeat ─────────────────────────────────────────────────────
            _ = hb_tick.tick() => {
                my_state.last_seen_ms = now_ms();
                send_swarm_msg(&engine, &SwarmMessage::Heartbeat(my_state.clone()));
                println!(
                    "💓  [HEARTBEAT] Sent │ role: {:?} │ status: {:?} │ t={}",
                    my_state.role, my_state.status, my_state.last_seen_ms
                );
            }

            // ── Stale peer detector ───────────────────────────────────────────
            _ = stale_tick.tick() => {
                let now = now_ms();
                if peer_map.is_empty() {
                    // Remind the operator what's needed
                    println!(
                        "🔍  [WAITING]  No messages committed yet — ensure ALL {} nodes are running.",
                        configs.len()
                    );
                } else {
                    for (id, ps) in &peer_map {
                        let age_ms = now.saturating_sub(ps.last_seen_ms);
                        if age_ms > stale_ms {
                            println!(
                                "⚠️   [STALE]   Peer …{} │ role: {:?} │ silent {:.1}s ← STALE",
                                short(id), ps.role, age_ms as f64 / 1_000.0
                            );
                        } else {
                            println!(
                                "✅  [ALIVE]   Peer …{} │ role: {:?} │ status: {:?} │ {:.1}s ago",
                                short(id), ps.role, ps.status, age_ms as f64 / 1_000.0
                            );
                        }
                    }
                }
            }

            // ── Role toggle (Agent A only, fires once) ────────────────────────
            _ = async {
                match toggle_at {
                    Some(dl) => sleep_until(dl).await,
                    None     => std::future::pending::<()>().await,
                }
            }, if !role_toggled => {
                my_state.role = Role::Scout;
                my_state.last_seen_ms = now_ms();
                role_toggled = true;
                send_swarm_msg(&engine, &SwarmMessage::StateUpdate(my_state.clone()));
                sep();
                println!(
                    "🔄  [ROLE TOGGLE] Agent A → {:?} │ broadcasting StateUpdate…",
                    my_state.role
                );
                sep();
            }

            // ── Inbound Vertex messages ───────────────────────────────────────
            result = engine.recv_message() => {
                match result {
                    Ok(Some(Message::Event(event))) => {
                        let was_empty = peer_map.is_empty();

                        // Collect peers that send us a Hello so we can Ack them.
                        let mut ack_needed: Vec<AgentState> = Vec::new();

                        for tx_bytes in event.transactions() {
                            if let Ok(msg) = bincode::deserialize::<SwarmMessage>(tx_bytes) {
                                let should_ack = process_incoming(
                                    msg, &mut peer_map, &my_state.peer_id, stale_ms,
                                );
                                if should_ack {
                                    if let Some(ps) = peer_map.values().last() {
                                        ack_needed.push(ps.clone());
                                    }
                                }
                            }
                        }

                        // On first consensus, broadcast Hello so late-starting peers
                        // know we exist.
                        if was_empty && !peer_map.is_empty() {
                            sep();
                            println!("🌐  [CONNECTED]  Consensus formed — broadcasting Hello");
                            sep();
                            my_state.last_seen_ms = now_ms();
                            send_swarm_msg(&engine, &SwarmMessage::Hello(my_state.clone()));
                            println!(
                                "   ↳ 🤝 [HELLO] Sent │ role: {:?} │ status: {:?}",
                                my_state.role, my_state.status
                            );
                        }

                        // Send HelloAck for each new peer that sent us a Hello.
                        for peer in ack_needed {
                            my_state.last_seen_ms = now_ms();
                            send_swarm_msg(&engine, &SwarmMessage::HelloAck(my_state.clone()));
                            println!(
                                "   ↳ 🤝 [HELLO BACK] → peer …{} │ handshake complete ✓",
                                short(&peer.peer_id)
                            );
                        }
                    }
                    Ok(Some(Message::SyncPoint(_))) => {}
                    Ok(None) | Err(_) => {
                        println!("⛔  Engine closed.");
                        break;
                    }
                }
            }
        }
    }

    Ok(())
}

// ── Inbound message handler ────────────────────────────────────────────────────
/// Returns `true` if the message was a Hello from a brand-new peer
/// (i.e., the caller should send a HelloAck to complete the handshake).
fn process_incoming(
    msg:      SwarmMessage,
    peer_map: &mut HashMap<String, AgentState>,
    my_id:    &str,
    stale_ms: u64,
) -> bool {
    let (label, state, is_hello) = match &msg {
        SwarmMessage::Hello(s)    => ("🤝  [HELLO]       ", s.clone(), true),
        SwarmMessage::HelloAck(s) => ("🤝  [HELLO BACK]   ", s.clone(), false),
        SwarmMessage::Heartbeat(s)   => ("💓  [HEARTBEAT]   ", s.clone(), false),
        SwarmMessage::StateUpdate(s) => ("🔄  [STATE UPDATE]", s.clone(), false),
    };

    if state.peer_id == my_id { return false; }

    let age_ms = now_ms().saturating_sub(state.last_seen_ms);
    println!(
        "{} Peer …{} │ role: {:?} │ status: {:?} │ msg age {}ms",
        label, short(&state.peer_id), state.role, state.status, age_ms
    );

    if matches!(msg, SwarmMessage::HelloAck(_)) {
        println!(
            "   ↳ ✅ [HANDSHAKE COMPLETE] Session established with peer …{}",
            short(&state.peer_id)
        );
    }

    if matches!(msg, SwarmMessage::StateUpdate(_)) {
        println!(
            "   ↳ ✅ [MIRROR]   Replicated: peer …{} is now {:?}",
            short(&state.peer_id), state.role
        );
    }

    // Recovery detection
    if let Some(prev) = peer_map.get(&state.peer_id) {
        let prev_age = now_ms().saturating_sub(prev.last_seen_ms);
        if prev_age > stale_ms {
            println!(
                "   ↳ 🔁 [RECOVERY]  Peer …{} back after {:.1}s — connection resumed",
                short(&state.peer_id), prev_age as f64 / 1_000.0
            );
        }
    }

    // Is this a Hello from someone we've never seen? → caller should Ack.
    let is_new_peer = !peer_map.contains_key(&state.peer_id);

    let mut fresh = state;
    fresh.last_seen_ms = now_ms();
    peer_map.insert(fresh.peer_id.clone(), fresh);

    is_hello && is_new_peer
}